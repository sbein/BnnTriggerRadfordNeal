//-----------------------------------------------------------------------------
// Compute average over networks. Default is to use all networks
//-----------------------------------------------------------------------------

std::vector<double> Eff_MetMhtSextetReal_CenterUpDown(std::vector<double>& inputs,
                                                      int first=0,
                                                      int last=nnetworks-1)
{
  if ( first < 0 ) first = 0;
  if ( last  < 0 ) last  = nnetworks-1;
  if ( last  > nnetworks-1 ) last = nnetworks-1;
  if ( first > last )
    {
      std::cerr << "** MetMhtSextetReal ** first-index > last-index"
                << std::endl; exit(3);
    }

  // Compute average over networks
  std::vector<double> in(ninputs);
  in[0] = (inputs[0] - mean[0]) / sigma[0];
  in[1] = (inputs[1] - mean[1]) / sigma[1];
  in[2] = (inputs[2] - mean[2]) / sigma[2];

  std::vector<double> v;
  for(int i=first; i <= last; i++) v.push_back(netfun(in, weight[i]));
  std::sort(v.begin(), v.end());
  double median = v[int(nnetworks/2)];
  double up = 0.5*(v[int(0.84135*nnetworks)]+v[int(0.84135*nnetworks)+1]);
  double down = 0.5*(v[int(0.15865*nnetworks)]+v[int(0.15865*nnetworks)+1]);
  std::vector<double> CenterUpDown;
  CenterUpDown.push_back(median);
  CenterUpDown.push_back(up);
  CenterUpDown.push_back(down);
  return CenterUpDown;
  
}

std::vector<double> Eff_MetMhtSextetReal_CenterUpDown(double ht,
                                                      double met,
                                                      double njets,
                                                      int first=0, int last=nnetworks-1)
{
  std::vector<double> in(ninputs);
  in[0] = ht;
  in[1] = met;
  in[2] = njets;
  return Eff_MetMhtSextetReal_CenterUpDown(in, first, last);
}
